import * as cdk from 'aws-cdk-lib';
import * as glue from 'aws-cdk-lib/aws-glue';
import * as iam from 'aws-cdk-lib/aws-iam';
import * as events from 'aws-cdk-lib/aws-events';
import * as targets from 'aws-cdk-lib/aws-events-targets';
import { Construct } from 'constructs';

export interface GlueConstructProps {
  sourceBucketName: string;
  sourceBucketPrefix: string;
  dataFormat: string;
  crawlerSchedule: string;
}

export class GlueConstruct extends Construct {
  public readonly database: glue.CfnDatabase;
  public readonly crawler: glue.CfnCrawler;
  public readonly crawlerRole: iam.Role;
  public readonly databaseName: string;
  public readonly tableName: string;

  constructor(scope: Construct, id: string, props: GlueConstructProps) {
    super(scope, id);

    // Define resource names
    this.databaseName = 'kiro-user-dashboard-db';
    const crawlerName = 'kiro-user-dashboard-crawler';
    const crawlerRoleName = 'kiro-user-dashboard-crawler-role';
    
    // Table name will be auto-generated by crawler from S3 path
    // Typically it will be the last part of the prefix or bucket name
    const prefixParts = props.sourceBucketPrefix.split('/').filter(p => p.length > 0);
    this.tableName = prefixParts.length > 0 ? prefixParts[prefixParts.length - 1] : props.sourceBucketName;

    // Create Glue Database
    this.database = new glue.CfnDatabase(this, 'Database', {
      catalogId: cdk.Stack.of(this).account,
      databaseInput: {
        name: this.databaseName,
        description: 'Database for Kiro User Dashboard analytics',
      },
    });

    // Create IAM role for Glue Crawler with S3 read permissions
    this.crawlerRole = new iam.Role(this, 'CrawlerRole', {
      roleName: crawlerRoleName,
      assumedBy: new iam.ServicePrincipal('glue.amazonaws.com'),
      description: 'IAM role for Kiro User Dashboard Glue Crawler',
      managedPolicies: [
        iam.ManagedPolicy.fromAwsManagedPolicyName('service-role/AWSGlueServiceRole'),
      ],
    });

    // Add inline policy for S3 read permissions
    this.crawlerRole.addToPolicy(
      new iam.PolicyStatement({
        effect: iam.Effect.ALLOW,
        actions: ['s3:GetObject', 's3:ListBucket'],
        resources: [
          `arn:aws:s3:::${props.sourceBucketName}`,
          `arn:aws:s3:::${props.sourceBucketName}/${props.sourceBucketPrefix}*`,
        ],
      })
    );

    // Create Glue Crawler with S3 target configuration
    this.crawler = new glue.CfnCrawler(this, 'Crawler', {
      name: crawlerName,
      role: this.crawlerRole.roleArn,
      databaseName: this.databaseName,
      targets: {
        s3Targets: [
          {
            path: `s3://${props.sourceBucketName}/${props.sourceBucketPrefix}`,
          },
        ],
      },
      schemaChangePolicy: {
        updateBehavior: 'LOG',
        deleteBehavior: 'LOG',
      },
      recrawlPolicy: {
        recrawlBehavior: 'CRAWL_EVERYTHING',
      },
      configuration: JSON.stringify({
        Version: 1.0,
        CrawlerOutput: {
          Partitions: { AddOrUpdateBehavior: 'InheritFromTable' },
        },
      }),
    });

    // Ensure crawler is created after database
    this.crawler.addDependency(this.database);

    // Configure crawler schedule using EventBridge rule (default: 1:30 AM)
    const scheduleRule = new events.Rule(this, 'CrawlerSchedule', {
      schedule: events.Schedule.expression(props.crawlerSchedule),
      description: 'Schedule for Kiro User Dashboard Glue Crawler',
    });

    // Add crawler as target to the schedule rule
    scheduleRule.addTarget(
      new targets.AwsApi({
        service: 'Glue',
        action: 'startCrawler',
        parameters: {
          Name: crawlerName,
        },
      })
    );
  }
}
